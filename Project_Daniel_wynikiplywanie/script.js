// Funkcja konwertujƒÖca czas z min na sek
function convertTimeToSeconds(timeString) {
    if (timeString.includes(':')) {
        const [minutes, seconds] = timeString.split(':');
        return parseInt(minutes, 10) * 60 + parseFloat(seconds);
    }
    return parseFloat(timeString);
}

// Funkcja generujƒÖca tabelƒô
let dataToRender = []; // Deklaracja zmiennej w zasiƒôgu globalnym

function generateTable(data) {
    const tableBody = document.querySelector('#results-table tbody');
    tableBody.innerHTML = ''; // Wyczy≈õƒá poprzednie dane

    // Oblicz PB dla zawodnik√≥w na podstawie danych
    const personalBests = calculatePB(data); // Wywo≈Çanie funkcji calculatePB

    const dataToRender = filteredData.length > 0 ? filteredData : data;

    // Oblicz isPB dla ka≈ºdego rekordu (przed generowaniem wierszy)
    dataToRender.forEach(record => {
        const timeInSeconds = convertTimeToSeconds(record.time);
        record.isPB = timeInSeconds === personalBests[record.name]; // True, je≈õli to PB
    });

    dataToRender.forEach((record, index) => {
        const row = document.createElement('tr');

        // Dodaj numeracjƒô rzƒôd√≥w
        const rowNumberCell = document.createElement('td');
        rowNumberCell.textContent = index + 1; // Numer wiersza (1-based)
        rowNumberCell.classList.add('row-number'); // Klasa dla stylizacji
        row.appendChild(rowNumberCell);

        // Tworzenie kom√≥rek dla istniejƒÖcych kolumn
        Object.entries(record).forEach(([key, value]) => {
            // Pomi≈Ñ renderowanie isPB (nie chcemy go w UI)
            if (key === 'isPB') return;

            const cell = document.createElement('td');
            cell.textContent = value;
            row.appendChild(cell);
        });

        // Tworzenie kom√≥rki PB
        const pbCell = document.createElement('td');
        pbCell.classList.add('pb-cell');

        const timeInSeconds = convertTimeToSeconds(record.time); // U≈ºycie funkcji konwertujƒÖcej

        // Je≈õli czas to PB, dodaj ikonƒô
        if (timeInSeconds === personalBests[record.name]) {
            const icon = document.createElement('span');
            icon.classList.add('pb-checked');
            pbCell.appendChild(icon);
        }

        row.appendChild(pbCell); // Dodaj kom√≥rkƒô PB do wiersza

        // Tworzenie kom√≥rki "Medale"
        const medalCell = document.createElement('td');
        medalCell.classList.add('medal-cell');
        const recordPlace = parseInt(record.place, 10);

        // Przypisanie emotki medalu w zale≈ºno≈õci od miejsca
        if (recordPlace === 1) {
            medalCell.textContent = "ü•á"; // Z≈Çoty medal
        } else if (recordPlace === 2) {
            medalCell.textContent = "ü•à"; // Srebrny medal
        } else if (recordPlace === 3) {
            medalCell.textContent = "ü•â"; // BrƒÖzowy medal
        } else {
            medalCell.textContent = ""; // Brak medalu
        }

        row.appendChild(medalCell); // Dodaj kom√≥rkƒô "Medale" do wiersza

        tableBody.appendChild(row); // Dodaj wiersz do tabeli
    });
}




// Funkcja do aktualizacji ikon sortowania
function updateSortIcons() {
    // Czyszczenie ikon sortowania
    document.querySelectorAll('span[id$="-sort"]').forEach(span => {
        span.textContent = '';
    });

    // Ustawienie ikony dla aktywnej kolumny
    for (const [key, direction] of Object.entries(sortDirections)) {
        const span = document.getElementById(`${key}-sort`);
        if (direction === 'asc') {
            span.textContent = '‚¨Ä'; // RosnƒÖco
        } else {
            span.textContent = '‚¨Ç'; // MalejƒÖco
        }
    }
}
// Funkcja konwersujƒÖca wynik na sekundy
function convertTimeToSeconds(timeString) {
    // Je≈õli czas jest w formacie `mm:ss.sss`
    if (timeString.includes(':')) {
        const [minutes, seconds] = timeString.split(':');
        return parseInt(minutes, 10) * 60 + parseFloat(seconds);
    }
    // Je≈õli czas jest w formacie `ss.sss`
    return parseFloat(timeString);
}

// Funkcja sortujƒÖca
let filteredData = []; // Przechowuje aktualnie przefiltrowane dane

function sortTable(column) {
    const direction = sortDirections[column]; // Pobierz aktualny kierunek sortowania

    // Wybierz dane do sortowania: u≈ºyj filtrowanych danych, je≈õli sƒÖ dostƒôpne
    const dataToSort = filteredData.length > 0 ? filteredData : tableData;

    dataToSort.sort((a, b) => {
        let comparison = 0;

        if (column === 'date') {
            comparison = new Date(a[column]) - new Date(b[column]); // Por√≥wnanie dat
        } else if (column === 'time') {
            const timeToSeconds = time => convertTimeToSeconds(time);
            comparison = timeToSeconds(a[column]) - timeToSeconds(b[column]); // Por√≥wnanie czasu
        } else if (column === 'pts' || column === 'place' ) {
            comparison = parseInt(a[column]) - parseInt(b[column]); // Por√≥wnanie liczbowe
        } else if (column === 'PB') {
            comparison = (a.isPB === b.isPB) ? 0 : a.isPB ? -1 : 1; // Sortuj PB jako pierwsze
        } else {
            comparison = a[column].localeCompare(b[column]); // Por√≥wnanie tekstowe
        }

        return direction === 'asc' ? comparison : -comparison; // Uwzglƒôdnienie kierunku
    });

    // Prze≈ÇƒÖcz kierunek sortowania
    sortDirections[column] = direction === 'asc' ? 'desc' : 'asc';

    updateSortIcons(); // Aktualizacja ikon sortowania
    generateTable(dataToSort); // Od≈õwie≈º tabelƒô z posortowanymi danymi
}

function filterDataBySelectedNames(data) {
    const selectedNames = Array.from(document.querySelectorAll('input[name="name-filter"]:checked'))
        .map(checkbox => checkbox.value);

    filteredData = data.filter(record => selectedNames.includes(record.name)); // Aktualizacja filtrowanych danych
    return filteredData;
}


